const onKeyDown = function (e) {
  switch (e.code) {
    case "KeyW":
      controls.moveForward(speed);
      break;
    case "KeyA":
      controls.moveRight(-speed);
      break;
    case "KeyD":
      controls.moveRight(speed);
      break;
    case "KeyS":
      controls.moveForward(-speed);
      break;
    default:
      break;
  }
};
document.addEventListener("keydown", onKeyDown, false);


